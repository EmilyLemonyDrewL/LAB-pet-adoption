  const renderToDom = (divId, htmlToRender) => {
    const selectedDiv = document.querySelector(divId);
    selectedDiv.innerHTML = htmlToRender;
  }

  const cardsOnDom = (pets) => {
    let domString ="";
    for (const pet of pets) {
      domString += `<div class="card" style="width: 18rem;">
    <img src=${pet.imageUrl} class="card-img-top" alt=${pet.name}/>
    <div class="card-body">
      <h5 class="card-title">${pet.name}</h5>
      <p>Color: ${pet.color}</p>
      <p class="card-text">${pet.specialSkill}</p>
      <p>Type: ${pet.type}</p>
      <button class="btn btn-success" id="delete--${pet.id}">Delete Pet</button>
       
    </div>
  </div>`;
    }

    renderToDom("#app", domString);
  }

//function to filter pets for specific type
const filter = (pets, typeString) => {
  const typeArray = [];


for (const pet of pets) {
  if (pet.type === typeString) {
    typeArray.push(pet);
  }
}

return cardsOnDom(typeArray);
}


//target the buttons
const showCatsButton = document.querySelector("#cat-btn");
const showDogsButton = document.querySelector("#dog-btn");
const showDinosButton = document.querySelector("#dino-btn");
const showAllPetsButton = document.querySelector("#all-pets-btn");

//add pets button, target it, and add click event
showAllPetsButton.addEventListener('click', () => {
  cardsOnDom(pets);
})

//add click event(s) for each button

showCatsButton.addEventListener('click', () => {
  const petCats = filter(pets, "cat");
  cardsOnDom(petCats);
});

showDogsButton.addEventListener('click', () => {
  const petDogs = filter(pets, "dog");
  cardsOnDom(petDogs);
});

showDinosButton.addEventListener('click', () => {
  const petDinos = filter(pets, "dino");
  cardsOnDom(petDinos);
});



//form function below

const form = document.querySelector('form');

const createPet = (e) => {
  e.preventDefault();

  const newPetObj = {
    id: pets.length + 1,
    name: document.querySelector("#name").value,
    color: document.querySelector("#color").value,
    specialSkill: document.querySelector("#specialSkill").value,
    type: document.querySelector('input[name="pet-type"]:checked').value,
    imageUrl: document.querySelector("#image").value
  }

  pets.push(newPetObj);
  cardsOnDom(pets);
  form.reset();
};

form.addEventListener('submit', createPet);


//add delete button

//target your div that has the id of app
const app = document.querySelector("#app");

//add an event listener that "listens" for clicks
app.addEventListener('click', (e) => {

  //the includes method compares a searchElement to elements of an array. Make sure that e.target.id includes "delete"
  if (e.target.id.includes("delete")) {
    // deconstruct, baby!
    const [, id] = e.target.id.split("--");


    //According to google, .findIndex is an array method that returns the index of a first element in an array that satifies the provided testing function. -1 is returned if no elements satisfy the testing function.
    const index = pets.findIndex(e => e.id === Number(id));

    pets.splice(index, 1);

    cardsOnDom(pets);
  }

})
